#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	i;
	size_t	s_len;

	s_len = ft_strlen((char *)s);
	str = ft_calloc(s_len + 1, sizeof(char));
	if (!str)
		return (NULL);
	i = -1;
	while (++i < s_len)
		str[i] = f(i, s[i]);
	str[i] = '\0';
	return (str);
}
